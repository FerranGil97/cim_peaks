async function resetAllShowOnMap() {
  console.log('🔄 Posant showOnMap: false a tots els cims...');
  const snapshot = await db.collection('summits').get();
  let count = 0;
  for (let i = 0; i < snapshot.docs.length; i += 500) {
    const chunk = snapshot.docs.slice(i, i + 500);
    const batch = db.batch();
    for (const doc of chunk) {
      batch.update(doc.ref, { showOnMap: false });
    }
    await batch.commit();
    count += chunk.length;
    console.log(`  🔁 ${count}/${snapshot.docs.length} resetejats...`);
  }
  console.log('✅ Tots els cims resetejats a showOnMap: false');
}

async function updateShowOnMap() {
  console.log(`🗺️  Marcant ${showOnMapIds.length} cims com showOnMap: true...`);
  let updated = 0;
  for (let i = 0; i < showOnMapIds.length; i += 500) {
    const chunk = showOnMapIds.slice(i, i + 500);
    const batch = db.batch();
    for (const id of chunk) {
      const ref = db.collection('summits').doc(id);
      // merge: true per no sobreescriure cap altre camp
      batch.set(ref, { showOnMap: true }, { merge: true });
    }
    await batch.commit();
    updated += chunk.length;
    console.log(`  ⬆️  ${updated}/${showOnMapIds.length} processats...`);
  }
  console.log(`\n✅ ${updated} cims marcats com showOnMap: true`);
}

async function main() {
  try {
    await resetAllShowOnMap();  // Pas 1: tot a false
    await updateShowOnMap();    // Pas 2: els del KML a true
    console.log('\n🎉 Actualització completada!');
    process.exit(0);
  } catch (error) {
    console.error('❌ Error:', error);
    process.exit(1);
  }
}

